public class IntegerMathOpsEx4 {
    public static void main(String[] args) {
        int a = 100;
        int b = 10;

        a = a + b;
        System.out.println("a: " + a);

        a = a - b;
        System.out.println("a: " + a);

        a = a * b;
        System.out.println("a: " + a);

        a = a / b;
        System.out.println("a: " + a);

        a = a % 8;
        System.out.println("a: " + a);
    }
}