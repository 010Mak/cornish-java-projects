public class IntegerMathOpsEx6 {
    public static void main(String[] args) {

        int a = 100;
        int b = 10;


        a += b;
        System.out.println("adding b to a, a = " + a);


        a -= b;
        System.out.println("subtracting b from a, a = " + a);


        a *= b;
        System.out.println("multiplying a by b, a = " + a);


        a /= b;
        System.out.println("dividing a by b, a = " + a);


        a %= 8;
        System.out.println("a mod 8, a = " + a);


    }
}
